    -d nikolaevcodev.ru \
    -d www.nikolaevcodev.ru \
    --non-interactive \
    --agree-tos \
    --email noldor123@yandex.ru \
    --preferred-challenges http

if [ $? -eq 0 ]; then
    echo "‚úÖ SSL —Å–µ—Ä—Ç–∏—Ñ–∏–∫–∞—Ç —É—Å–ø–µ—à–Ω–æ –ø–æ–ª—É—á–µ–Ω!"
